@dataclass
class PaperResult:
    """Resultado unificado de busca de artigo acadêmico."""
    title: str
    authors: list[str] = field(default_factory=list)
    year: int | None = None
    doi: str = ""
    citations: int = 0
    source: str = ""  # "arxiv", "semanticscholar", "scholarly", "crossref"
    abstract: str = ""
    url: str = ""
    pdf_url: str | None = None


class SeekerMultiSource:
    """Busca acadêmica multi-fonte usando bibliotecas do PyPI Scout.
    
    Afinidade: scholarly(95%), arxiv(95%), semanticscholar(95%), scihub-cn(95%)
    """

    @staticmethod
    def search_arxiv(query: str, max_results: int = 10) -> list[PaperResult]:
        """Busca no arXiv."""
        try:
            import arxiv
            client = arxiv.Client()
            search = arxiv.Search(
                query=query,
                max_results=max_results,
                sort_by=arxiv.SortCriterion.Relevance,
            )
            results = []
            for r in client.results(search):
                results.append(PaperResult(
                    title=r.title,
                    authors=[str(a) for a in r.authors],
                    year=r.published.year if r.published else None,
                    doi=getattr(r, 'doi', ''),
                    source="arxiv",
                    abstract=r.summary,
                    url=r.entry_id,
                    pdf_url=r.pdf_url,
                ))
            logger.info(f"arXiv: {len(results)} resultados para '{query}'")
            return results
        except ImportError:
            logger.warning("arxiv não instalado. Use: pip install arxiv")
            return []
        except Exception as e:
            logger.error(f"Erro arXiv: {e}")
            return []

    @staticmethod
    def search_semanticscholar(query: str, limit: int = 10) -> list[PaperResult]:
        """Busca no Semantic Scholar."""
        try:
            from semanticscholar import SemanticScholar
            sch = SemanticScholar(timeout=30)
            raw = sch.search_paper(query, limit=limit)
            results = []
            for r in raw:
                results.append(PaperResult(
                    title=getattr(r, 'title', ''),
                    authors=[str(a.get('name', '')) for a in getattr(r, 'authors', [])],
                    year=getattr(r, 'year', None),
                    doi=getattr(r, 'externalIds', {}).get('DOI', ''),
                    citations=getattr(r, 'citationCount', 0),
                    source="semanticscholar",
                    abstract=getattr(r, 'abstract', ''),
                    url=getattr(r, 'url', ''),
                ))
            logger.info(f"Semantic Scholar: {len(results)} resultados para '{query}'")
            return results
        except ImportError:
            logger.warning("semanticscholar não instalado. Use: pip install semanticscholar")
            return []
        except Exception as e:
            logger.error(f"Erro Semantic Scholar: {e}")
            return []

    @staticmethod
    def search_scholarly(query: str, max_results: int = 5) -> list[PaperResult]:
        """Busca no Google Scholar via scholarly."""
        try:
            from scholarly import scholarly, ProxyGenerator
            pg = ProxyGenerator()
            try:
                pg.FreeProxies()
                scholarly.use_proxy(pg)
            except Exception:
                pass  # Sem proxy, tenta direto
            
            search = scholarly.search_pubs(query)
            results = []
            for i, r in enumerate(search):
                if i >= max_results:
                    break
                bib = r.get('bib', {})
                results.append(PaperResult(
                    title=bib.get('title', ''),
                    authors=[bib.get('author', '')],
                    year=int(bib.get('pub_year', 0)) if bib.get('pub_year') else None,
                    source="scholarly",
                    abstract=bib.get('abstract', ''),
                    url=r.get('pub_url', ''),
                    citations=r.get('num_citations', 0),
                ))
            logger.info(f"Google Scholar: {len(results)} resultados para '{query}'")
            return results
        except ImportError:
            logger.warning("scholarly não instalado. Use: pip install scholarly")
            return []
        except Exception as e:
            logger.error(f"Erro scholarly: {e}")
            return []

    @staticmethod
    def search_all(query: str, max_per_source: int = 5) -> dict[str, list[PaperResult]]:
        """Busca em todas as fontes simultaneamente."""
        return {
            "arxiv": SeekerMultiSource.search_arxiv(query, max_per_source),
            "semanticscholar": SeekerMultiSource.search_semanticscholar(query, max_per_source),
            "scholarly": SeekerMultiSource.search_scholarly(query, max_per_source),
        }


