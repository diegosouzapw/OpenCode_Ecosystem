        """Extrai metadados de um PDF."""
        try:
            from pypdf import PdfReader
            reader = PdfReader(str(pdf_path))
            return {
                "pages": len(reader.pages),
                "metadata": dict(reader.metadata) if reader.metadata else {},
            }
        except ImportError:
            return {}
        except Exception:
            return {}


