class BioMedAnalyzer:
    """Análise biomédica: BioPython + PySUS (DATASUS).
    
    Afinidade: biopython(85%), pysus(90%)
    """

    @staticmethod
    def search_pubmed(query: str, max_results: int = 5) -> list[dict[str, Any]]:
        """Busca artigos no PubMed/NCBI via BioPython."""
        try:
            from Bio import Entrez
            Entrez.email = "opencode@ecosystem.br"
            handle = Entrez.esearch(db="pubmed", term=query, retmax=max_results)
            record = Entrez.read(handle)
            handle.close()

            ids = record.get("IdList", [])
            if not ids:
                return []

            handle = Entrez.efetch(db="pubmed", id=",".join(ids), rettype="xml", retmode="text")
            records = Entrez.read(handle)
            handle.close()

            results = []
            for article in records.get("PubmedArticle", []):
                medline = article.get("MedlineCitation", {}).get("Article", {})
                title = medline.get("ArticleTitle", "")
                authors_list = medline.get("AuthorList", [])
                authors = [
                    f"{a.get('LastName', '')} {a.get('ForeName', '')}"
                    for a in authors_list[:3]
                ]
                results.append({
                    "title": str(title),
                    "authors": authors,
                    "pmid": str(article.get("MedlineCitation", {}).get("PMID", "")),
                    "source": "PubMed/NCBI",
                })
            logger.info(f"PubMed: {len(results)} results for '{query}'")
            return results
        except ImportError:
            logger.warning("biopython not installed")
            return []
        except Exception as e:
            logger.error(f"PubMed: {e}")
            return []

