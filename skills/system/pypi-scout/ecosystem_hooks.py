    def get_stock_data(ticker: str, period: str = "1y") -> dict[str, Any]:
        """Obtém dados de ação via Yahoo Finance."""
        try:
            import yfinance as yf
            stock = yf.Ticker(ticker)
            info = stock.info
            hist = stock.history(period=period)
            return {
                "ticker": ticker,
                "name": info.get("longName", ticker),
                "sector": info.get("sector", ""),
                "market_cap": info.get("marketCap", 0),
                "current_price": info.get("currentPrice", 0),
                "pe_ratio": info.get("trailingPE", None),
                "dividend_yield": info.get("dividendYield", None),
                "history_rows": len(hist),
                "history_latest": hist.tail(5).to_dict() if not hist.empty else {},
            }
        except ImportError:
            return {"error": "yfinance not installed"}
        except Exception as e:
            return {"error": str(e)[:100]}

    @staticmethod
    def get_fred_series(series_id: str, api_key: str = "") -> Any:
        """Obtém série temporal do FRED (Federal Reserve).
        
        Precisa de API key gratuita: https://fred.stlouisfed.org/docs/api/api_key.html
        """
        try:
            from fredapi import Fred
            fred = Fred(api_key=api_key)
            return fred.get_series(series_id)
        except ImportError:
            return None
        except Exception as e:
            logger.error(f"FRED: {e}")
            return None

    @staticmethod
    def get_market_calendar(exchange: str = "NYSE", start: str = "2024-01-01", end: str = "2024-12-31") -> Any:
        """Obtém calendário de pregão."""
