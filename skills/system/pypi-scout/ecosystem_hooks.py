    def get_top_crypto(n: int = 5) -> list[dict[str, Any]]:
        """Top criptomoedas por volume (Binance)."""
        return MarketSpeculator._fetch_top_from_exchange("binance", n)

    @staticmethod
    def _fetch_top_from_exchange(exchange_id: str, n: int) -> list[dict[str, Any]]:
        try:
            import ccxt
            exchange = getattr(ccxt, exchange_id)({"enableRateLimit": True})
            markets = exchange.fetch_tickers()
            # Filtrar USDT pairs e ordenar por volume
            usdt_pairs = {
                k: v for k, v in markets.items()
                if k.endswith("/USDT") and v.get("quoteVolume", 0) > 0
            }
            sorted_pairs = sorted(
                usdt_pairs.items(),
                key=lambda x: x[1].get("quoteVolume", 0),
                reverse=True,
            )[:n]
            return [
                {
                    "symbol": k,
                    "price": v.get("last", 0),
                    "volume_24h": v.get("quoteVolume", 0),
                    "change_24h": v.get("percentage", 0),
                }
                for k, v in sorted_pairs
            ]
        except Exception as e:
            logger.error(f"Crypto fetch: {e}")
            return []


