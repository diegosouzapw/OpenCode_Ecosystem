class WorldBankAnalyzer:
    """Análise de dados do Banco Mundial para PhD Auditor.
    
    Afinidade: wbgapi(95%), data_analysis(95%)
    """

    @staticmethod
    def get_indicator_data(
        indicator: str,
        country: str = "BRA",
        years: range | None = None,
    ) -> dict[str, Any]:
        """Obtém dados de um indicador do World Bank.
        
        Args:
            indicator: Código do indicador (ex: 'NY.GDP.PCAP.CD' para PIB per capita)
            country: Código ISO do país (default: 'BRA')
            years: Range de anos (default: 2010-2024)
        
        Returns:
            Dict com valores anuais e metadados
        """
        if years is None:
            years = range(2010, 2025)
        
        try:
            import wbgapi as wb
            data: Any = wb.data.fetch(indicator, country, list(years))  # type: ignore[arg-type]
            series_info = wb.series.get(indicator)
            return {
                "indicator": indicator,
                "name": series_info.get("value", indicator) if series_info else indicator,  # type: ignore[union-attr]
                "country": country,
                "values": str(data),
                "source": "World Bank (wbgapi)",
            }
        except ImportError:
            logger.warning("wbgapi não instalado. Use: pip install wbgapi")
            return {"error": "wbgapi not installed"}
        except Exception as e:
            logger.error(f"Erro WBGAPI: {e}")
            return {"error": str(e)}

    @staticmethod
    def compare_countries(
        indicator: str,
        countries: list[str],
        year: int = 2023,
    ) -> dict[str, float]:
        """Compara indicador entre países."""
        try:
            import wbgapi as wb
            data: Any = wb.data.fetch(indicator, countries, [year])  # type: ignore[arg-type]
            return dict(data) if data else {}
        except ImportError:
            return {}
        except Exception:
            return {}

    @staticmethod
    def list_indicators(keyword: str = "", limit: int = 20) -> list[dict[str, str]]:
        """Lista indicadores disponíveis por palavra-chave."""
        try:
            import wbgapi as wb
            results = []
            for s in wb.series.list():
                name = s.get("value", "")
                if keyword.lower() in name.lower():
                    results.append({
                        "id": s.get("id", ""),
                        "name": name,
                        "source": s.get("source", {}).get("value", ""),
                    })
                    if len(results) >= limit:
                        break
            return results
        except ImportError:
            return []


